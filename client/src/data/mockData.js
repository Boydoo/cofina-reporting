// Statistiques globales calculées
export function getGlobalStats() {
  const totalCommitment = BAILLEURS.reduce((s, b) => s + b.commitment, 0)
  const totalDisbursed   = BAILLEURS.reduce((s, b) => s + b.disbursed,   0)
  const urgentBailleurs  = BAILLEURS.filter(b => b.status === 'urgent').length

  const latestClientsIndicator = INDICATORS.find(i => i.id === 'SOC-01')
  const latestClients = latestClientsIndicator?.data?.at(-1)?.actual ?? 0

  const latestWomenIndicator = INDICATORS.find(i => i.id === 'SOC-02')
  const latestWomen = latestWomenIndicator?.data?.at(-1)?.actual ?? 0

  const latestNPL = INDICATORS.find(i => i.id === 'FIN-02')?.data?.at(-1)?.actual ?? 0
  const latestCAR = INDICATORS.find(i => i.id === 'FIN-03')?.data?.at(-1)?.actual ?? 0

  const reportsSubmitted = REPORTS.filter(r => r.status === 'submitted').length
  const reportsPending   = REPORTS.filter(r => r.status !== 'submitted').length

  return {
    totalCommitment,
    totalDisbursed,
    disbursementRate: Math.round((totalDisbursed / totalCommitment) * 100),
    activeBailleurs: BAILLEURS.filter(b => b.status !== 'inactif').length,
    urgentBailleurs,
    totalIndicators: INDICATORS.length,
    latestClients,
    latestWomen,
    latestNPL,
    latestCAR,
    reportsSubmitted,
    reportsPending,
  }
}

